import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const publicDir = path.join(__dirname, "..", "public");

const DEFAULT_REPO = "https://github.com/cultpodcasts/apostate-leaks";

function resolveSha() {
  const fromCi =
    process.env.WORKERS_CI_COMMIT_SHA ||
    process.env.CF_PAGES_COMMIT_SHA ||
    process.env.GITHUB_SHA ||
    process.env.CI_COMMIT_SHA;
  if (fromCi) return fromCi;
  try {
    return execSync("git rev-parse HEAD", { encoding: "utf8" }).trim();
  } catch {
    return "";
  }
}

function resolveBranch() {
  return (
    process.env.WORKERS_CI_BRANCH ||
    process.env.CF_PAGES_BRANCH ||
    process.env.GITHUB_REF_NAME ||
    process.env.CI_COMMIT_BRANCH ||
    "main"
  );
}

const repository = process.env.GITHUB_REPOSITORY
  ? `https://github.com/${process.env.GITHUB_REPOSITORY}`
  : DEFAULT_REPO;

const commit = resolveSha();
const commitShort = commit ? commit.slice(0, 7) : "unknown";
const branch = resolveBranch();
const builtAt = new Date().toISOString();

const refForUrls = commit || "main";

const info = {
  repository,
  commit: commit || "unknown",
  commitShort,
  branch,
  builtAt,
  commitUrl: `${repository}/commit/${refForUrls}`,
  treeUrl: commit ? `${repository}/tree/${commit}` : repository,
  dataAuditPath: `${repository}/blob/${refForUrls}/offline-pipeline/src/audit-deploy.ts`,
};

const json = JSON.stringify(info, null, 2);
writeFileSync(path.join(publicDir, "build-info.json"), `${json}\n`, "utf8");

const js = `// Generated by scripts/write-build-info.mjs — do not edit
globalThis.__BUILD_INFO__ = ${JSON.stringify(info)};
`;
writeFileSync(path.join(publicDir, "build-info.js"), js, "utf8");

console.log(`Wrote build-info.json and build-info.js (${commitShort} @ ${branch})`);
